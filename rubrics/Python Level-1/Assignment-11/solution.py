# Assignment 11: While Loops

# 1. Print numbers 1 to 10
print("Numbers 1 to 10:")
i = 1
while i <= 10:
    print(i)
    i += 1

# 2. Print name 10 times with a serial number
print("\nName 10 times:")
i = 1
name = "Student"
while i <= 10:
    print(str(i) + ". " + name)
    i += 1

# 3. Print numbers 10 to 1
print("\nCountdown 10 to 1:")
i = 10
while i >= 1:
    print(i)
    i -= 1

# 4. Print all even numbers from 1 to 20
print("\nEven numbers from 1 to 20:")
i = 2
while i <= 20:
    print(i)
    i += 2

# 5. Print all the odd numbers from 19 to 1
print("\nOdd countdown from 19 to 1:")
i = 19
while i >= 1:
    print(i)
    i -= 2

# 6. Print all the multiples of 10 from 10 to 100
print("\nMultiples of 10 from 10 to 100:")
i = 10
while i <= 100:
    print(i)
    i += 10

# 1. Four Arithmetic Operators
# The four basic arithmetic operators are:
# + (Addition)
# - (Subtraction)
# * (Multiplication)
# / (Division)

# Creating two variables with numbers
num1 = 12
num2 = 4

# Showing four examples using the print() function
print("Addition:", num1 + num2)       # 12 + 4 = 16
print("Subtraction:", num1 - num2)    # 12 - 4 = 8
print("Multiplication:", num1 * num2) # 12 * 4 = 48
print("Division:", num1 / num2)        # 12 / 4 = 3.0


# 1. Programming Language
print("Programming Language: A formal set of instructions used to communicate with a computer and direct it to perform specific tasks.")

# 2. Pseudocode
print("Pseudocode: An informal way of writing out program steps using plain English before converting it into actual code.")

# 3. Syntax
print("Syntax: The specific set of rules that defines how a program must be structured and written for the computer to understand it.")

# 4. Variable
print("Variable: A reserved memory location that acts as a container to store data values.")

# 5. Integer Numbers
print("Integer Numbers: Positive or negative whole numbers without any decimal points (represented as 'int' in Python).")

# 6. String
print("String: A sequence of characters (letters, numbers, or symbols) enclosed in single or double quotes.")

# 7. Boolean
print("Boolean: A data type that can only have one of two values: True or False.")

# 8. Three conditional statements
print("Three conditional statements: Python uses if, elif (short for else if), and else to make decisions based on conditions.")

# 9. input()
print("input(): A built-in function that allows a program to pause and receive text input from the user via the keyboard.")

